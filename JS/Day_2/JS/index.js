//EX1:How to know month of a year

let month = 12;
if (month >= 1 && month <= 3) {
    console.log('Spring!')
} else if (month >= 4 && month <= 6) {
    console.log('Summer!')
} else if (month >= 7 && month <= 9) {
    console.log('Autumn!')
} else if (month >= 10 && month <= 12) {
    console.log('Winter!')
} else {
    console.log('Month is wrong!')
}